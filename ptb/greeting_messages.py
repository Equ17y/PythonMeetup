# Приветственные сообщения для разных ролей

def get_welcome_message(role, user_name):
    """
    Возвращает приветственное сообщение с
    кратким описанием функционала в зависимости от роли
    """
    messages = {
        "guest": f'''Добро пожаловать, {user_name}!

Доступные функции:
• Просмотр программ мероприятий.
• Просмотр предстоящих мероприятий,
  а также подписка на мероприятия.

Используйте кнопки ниже для навигации.''',

        "speaker": f'''Добро пожаловать, {user_name}!

Вы вошли как **спикер**. Доступные функции:
• Завершение текущего выступления.
• Просмотр программ мероприятий.
• Просмотр предстоящих мероприятий,
  а также подписка на мероприятия.

Используйте кнопки ниже для навигации.''',

        "organizer": f'''Добро пожаловать, {user_name}!

Вы вошли как **организатор**. Доступные команды:
• Просмотр программ мероприятий.
• Рассылка сообщений всем пользователям.

Используйте кнопки ниже для навигации.'''
    }

    return messages.get(role, messages["guest"])
